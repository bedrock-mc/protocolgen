package main

import (
	"fmt"
	"go/format"
	"sort"
	"strings"
)

// rawFormat generates a self-contained Go package: a struct (with ID and Marshal)
// for every packet, a struct (with Marshal) for every composite definition, and a
// minimal IO interface to marshal against. No gophertunnel dependency, doc-faithful
// names. Fields the docs cannot express (oneOf unions) are flagged with a TODO.
type rawFormat struct{ pkg string }

func (rawFormat) Name() string { return "raw" }

// EmitPacket renders one packet. The doc title (with its "Packet" suffix) is used
// as the type name so it cannot collide with a same-named composite definition.
func (r rawFormat) EmitPacket(pk genPacket, _ string) (string, []string, error) {
	body, todos := r.emitType(pk.Title, pk.Fields, pk.ID, true)
	src := "package " + r.pkg + "\n\n" + body
	formatted, err := format.Source([]byte(src))
	if err != nil {
		return src, todos, fmt.Errorf("gofmt: %w", err)
	}
	return string(formatted), todos, nil
}

// EmitExtra renders the IO interface (protocol.go) and every composite type
// (types.go). Any composite type referenced by a packet or composite but not
// defined in the docs is emitted as a stub so the package always compiles.
func (r rawFormat) EmitExtra(comps []composite, packets []genPacket) (map[string]string, error) {
	files := map[string]string{"protocol.go": rawIOSource(r.pkg)}

	var b strings.Builder
	b.WriteString("// Code generated by protocolgen (raw). DO NOT EDIT.\n\n")
	b.WriteString("package " + r.pkg + "\n\n")

	generated := map[string]bool{}
	for _, c := range comps {
		generated[c.GoName] = true
		body, _ := r.emitType(c.GoName, c.Fields, 0, false)
		b.WriteString(body)
		b.WriteString("\n")
	}

	// Stub any referenced-but-undefined composite type (0-property/opaque defs,
	// or types whose definition is absent from the docs).
	refs := map[string]bool{}
	for _, p := range packets {
		compositeRefsInFields(p.Fields, refs)
	}
	for _, c := range comps {
		compositeRefsInFields(c.Fields, refs)
	}
	var missing []string
	for name := range refs {
		if name != "" && !generated[name] {
			missing = append(missing, name)
		}
	}
	sort.Strings(missing)
	for _, name := range missing {
		fmt.Fprintf(&b, "// %s is referenced by the docs but has no generated definition. TODO: complete.\ntype %s struct{}\n\nfunc (pk *%s) Marshal(io IO) {}\n\n", name, name, name)
	}

	formatted, err := format.Source([]byte(b.String()))
	if err != nil {
		files["types.go"] = b.String()
		return files, fmt.Errorf("gofmt types.go: %w", err)
	}
	files["types.go"] = string(formatted)
	return files, nil
}

// compositeRefsInFields records the composite type names referenced by fields.
func compositeRefsInFields(fields []genField, into map[string]bool) {
	for _, f := range fields {
		switch {
		case f.Composite:
			into[cleanCompositeTitle(f.RefTitle)] = true
		case f.IsArray && f.Elem != nil && f.Elem.Composite:
			into[cleanCompositeTitle(f.Elem.RefTitle)] = true
		}
	}
}

// emitType renders a struct plus its Marshal method (and ID for packets). Field
// names are made safe against the type's methods (ID/Marshal) and deduplicated.
func (r rawFormat) emitType(typeName string, fields []genField, id int, isPacket bool) (string, []string) {
	var todos []string
	type resolved struct {
		name  string
		shape fieldShape
	}
	rs := make([]resolved, 0, len(fields))
	used := map[string]int{}
	for _, f := range fields {
		f.GoName = uniqueName(reservedSafe(f.GoName, isPacket), used)
		sh := resolveRaw(f)
		rs = append(rs, resolved{f.GoName, sh})
		if sh.Todo != "" {
			todos = append(todos, fmt.Sprintf("%s.%s: %s", typeName, f.GoName, sh.Todo))
		}
	}

	var b strings.Builder
	fmt.Fprintf(&b, "type %s struct {\n", typeName)
	for _, r := range rs {
		fmt.Fprintf(&b, "\t%s %s\n", r.name, r.shape.GoType)
	}
	b.WriteString("}\n\n")
	if isPacket {
		fmt.Fprintf(&b, "func (*%s) ID() uint32 { return %d }\n\n", typeName, id)
	}
	fmt.Fprintf(&b, "func (pk *%s) Marshal(io IO) {\n", typeName)
	for _, r := range rs {
		fmt.Fprintf(&b, "\t%s\n", r.shape.Marshal)
	}
	b.WriteString("}\n")
	return b.String(), todos
}

// reservedSafe renames a field that would collide with the generated methods.
func reservedSafe(name string, isPacket bool) string {
	if name == "Marshal" || (isPacket && name == "ID") {
		return name + "Field"
	}
	return name
}

// uniqueName disambiguates a repeated field name within one struct.
func uniqueName(name string, used map[string]int) string {
	if used[name] == 0 {
		used[name]++
		return name
	}
	for {
		used[name]++
		cand := fmt.Sprintf("%s%d", name, used[name])
		if used[cand] == 0 {
			used[cand]++
			return cand
		}
	}
}

// resolveRaw maps a doc field to its raw Go type and Marshal statement. Unlike
// the gophertunnel resolver it never references protocol.* — composites resolve
// to locally-generated struct types.
func resolveRaw(f genField) fieldShape {
	ref := "&pk." + f.GoName
	switch {
	case f.OneOf:
		return fieldShape{GoType: "any", Marshal: fmt.Sprintf("_ = pk.%s // TODO: oneOf union", f.GoName), Todo: "oneOf union"}
	case f.IsArray:
		elem := *f.Elem
		if elem.Composite {
			t := cleanCompositeTitle(elem.RefTitle)
			return fieldShape{GoType: "[]" + t, Marshal: fmt.Sprintf("Slice(io, &pk.%s)", f.GoName)}
		}
		gt, op, ok := scalarIO(elem.Under, elem.JSONType, elem.Options)
		if !ok {
			return fieldShape{GoType: "[]any", Marshal: fmt.Sprintf("_ = pk.%s // TODO: array of unmapped type", f.GoName), Todo: "array of unmapped type"}
		}
		return fieldShape{GoType: "[]" + gt, Marshal: fmt.Sprintf("SliceOf(io, &pk.%s, io.%s)", f.GoName, op)}
	case f.Composite:
		t := cleanCompositeTitle(f.RefTitle)
		return fieldShape{GoType: t, Marshal: fmt.Sprintf("pk.%s.Marshal(io)", f.GoName)}
	case isNBT(f):
		return fieldShape{GoType: "[]byte", Marshal: fmt.Sprintf("io.Bytes(%s)", ref)}
	case f.Enum && !hasOption(f.Options, "Enum-as-Value"):
		return fieldShape{GoType: "string", Marshal: fmt.Sprintf("io.String(%s)", ref)}
	}
	gt, op, ok := scalarIO(f.Under, f.JSONType, f.Options)
	if !ok {
		return fieldShape{GoType: "any", Marshal: fmt.Sprintf("_ = pk.%s // TODO: unmapped type %q", f.GoName, f.Under), Todo: "unmapped type " + f.Under}
	}
	return fieldShape{GoType: gt, Marshal: fmt.Sprintf("io.%s(%s)", op, ref)}
}

// rawIOSource returns the generated IO interface and slice helpers.
func rawIOSource(pkg string) string {
	return `// Code generated by protocolgen (raw). DO NOT EDIT.

package ` + pkg + `

// IO is a bidirectional (de)serialiser: when reading it fills the pointers, when
// writing it consumes them. Provide an implementation to (un)marshal packets.
type IO interface {
	Bool(*bool)
	Int8(*int8)
	Uint8(*uint8)
	Int16(*int16)
	Uint16(*uint16)
	Int32(*int32)
	Uint32(*uint32)
	Int64(*int64)
	Uint64(*uint64)
	Varint32(*int32)
	Varuint32(*uint32)
	Varint64(*int64)
	Varuint64(*uint64)
	Float32(*float32)
	Float64(*float64)
	String(*string)
	Bytes(*[]byte)
}

// Marshaler is implemented by every generated packet and composite type.
type Marshaler interface{ Marshal(IO) }

// Slice (de)serialises a varuint32-length-prefixed slice of Marshaler values.
func Slice[T any, PT interface {
	*T
	Marshaler
}](io IO, s *[]T) {
	n := uint32(len(*s))
	io.Varuint32(&n)
	if uint32(len(*s)) != n {
		*s = make([]T, n)
	}
	for i := range *s {
		PT(&(*s)[i]).Marshal(io)
	}
}

// SliceOf (de)serialises a varuint32-length-prefixed slice of scalar values using
// the given primitive IO method.
func SliceOf[T any](io IO, s *[]T, f func(*T)) {
	n := uint32(len(*s))
	io.Varuint32(&n)
	if uint32(len(*s)) != n {
		*s = make([]T, n)
	}
	for i := range *s {
		f(&(*s)[i])
	}
}
`
}

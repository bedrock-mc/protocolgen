package main

import (
	"fmt"
	"go/format"
	"sort"
	"strings"
)

const protocolImport = "github.com/sandertv/gophertunnel/minecraft/protocol"

// emitPacket renders a genPacket into gofmt'd gophertunnel-format Go source,
// returning the source and the list of TODO notes for fields needing hand work.
func emitPacket(pk genPacket, idConst string) (source string, todos []string, err error) {
	if idConst == "" {
		idConst = "ID" + pk.TypeName
	}

	imports := map[string]bool{protocolImport: true}
	var structFields, marshalLines []string

	for _, f := range pk.Fields {
		shape := resolve(f)
		for _, imp := range shape.Imports {
			imports[imp] = true
		}
		if shape.Todo != "" {
			todos = append(todos, fmt.Sprintf("%s.%s: %s", pk.TypeName, f.GoName, shape.Todo))
		}
		structFields = append(structFields, fmt.Sprintf("\t%s %s", f.GoName, shape.GoType))
		marshalLines = append(marshalLines, "\t"+shape.Marshal)
	}

	var b strings.Builder
	b.WriteString("package packet\n\n")

	b.WriteString("import (\n")
	for _, imp := range sortedKeys(imports) {
		fmt.Fprintf(&b, "\t%q\n", imp)
	}
	b.WriteString(")\n\n")

	fmt.Fprintf(&b, "// %s was generated by protocolgen from the Mojang bedrock-protocol-docs.\n", pk.TypeName)
	fmt.Fprintf(&b, "type %s struct {\n%s\n}\n\n", pk.TypeName, strings.Join(structFields, "\n"))

	fmt.Fprintf(&b, "// ID ...\nfunc (*%s) ID() uint32 {\n\treturn %s\n}\n\n", pk.TypeName, idConst)

	fmt.Fprintf(&b, "func (pk *%s) Marshal(io protocol.IO) {\n%s\n}\n", pk.TypeName, strings.Join(marshalLines, "\n"))

	formatted, ferr := format.Source([]byte(b.String()))
	if ferr != nil {
		// Return the unformatted source too, to aid debugging of a bad emit.
		return b.String(), todos, fmt.Errorf("gofmt: %w", ferr)
	}
	return string(formatted), todos, nil
}

func sortedKeys(m map[string]bool) []string {
	keys := make([]string, 0, len(m))
	for k := range m {
		keys = append(keys, k)
	}
	sort.Strings(keys)
	return keys
}
